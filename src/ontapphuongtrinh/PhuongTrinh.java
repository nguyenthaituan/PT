public class PhuongTrinh {
    float a,b,c;

    public PhuongTrinh(float a, float b, float c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public PhuongTrinh() {
    }

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }

    public float getC() {
        return c;
    }

    public void setC(float c) {
        this.c = c;
    }
    
    
}
